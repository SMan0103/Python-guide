# Der kan vi nemt fixe det ved at lave en Else funktion som er en funktion som høre inde under if funktion 
# Her vil jeg vise hvordan se vil se ud 

x = 2
y = 1
if x == y :
    print("X er lige så stor som y")
else:
    print("X og y er ikke det samme")
# Her vil den printe det der stor i else fordi at x og y er ikke det samme. 

