# If funktioner er en funktion som der fortæller hvis en ting er på en måde så skal der ske noget.
# Det kan ses på den funktion nede under.
x = 1 
y = 1 
if x == y :
    print("X er lige så stor som y")
# Her vil den printe det der står i print funktionen. 
# Det giver også god mening når vi siger at x == y hvor begge har samme værdi gemt i sig. 

